<?php

    function soma($num1, $num2){
        $result = $num1 + $num2;
        return($result);   
    }
    function sub($num1, $num2){
        $result = $num1 - $num2;
        return($result);   
    }
    function mult($num1, $num2){
        $result = $num1 * $num2;
        return($result);   
    }
    function div($num1, $num2){
        $result = $num1 / $num2;
        return($result);   
    }

?>